public class vector{
	private int x1;
	private int y1;
	private int x2;
	private int y2;
	
	public vector(int a,int  b,int  c, int d ){

		this.x1 = a;
		this.x2 = c;
		this.y1 = b;
		this.y2 = d;

	}


	public int getX1(){
		return this.x1;
	}


	public int getY1(){
		return this.y1;
	}


	public int getY2(){
		return this.y2;
	}


	public int getX2(){
		return this.x2;
	}
	
}